public class Faculty extends Employee {

    private int officeHours;
    private String rank;

    public Faculty() {

    }

    public int getOfficeHours() {
        return officeHours;
    }

    public void setOfficeHours() {
        this.officeHours = officeHours;
    }

    public String getRank() {
        return rank;
    }

    public void setRank() {
        this.rank = rank;
    }
}
