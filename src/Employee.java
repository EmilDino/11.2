public class Employee extends Person {

    private String office;
    private int salary;

    public Employee () {

    }

    public String getOffice() {
        return office;
    }

    public void setOffice() {
        this.office = office;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary() {
        this.salary = salary;
    }
}
